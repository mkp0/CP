void solve()
{
    ll a[3];
    ll b[3];
    ll c[3];

    cin >> a[0];
    a[1] = a[0] + 1;
    a[2] = a[0] - 1;

    cin >> b[0];
    b[1] = b[0] + 1;
    b[2] = b[0] - 1;

    cin >> c[0];
    c[1] = c[0] + 1;
    c[2] = c[0] - 1;

    ll ans = INT_MAX;
    for (int i = 0; i < 3; i++)
    {
        for (int j = 0; j < 3; j++)
        {
            for (int k = 0; k < 3; k++)
            {
                ans = min(ans, abs(a[i] - b[j]) + abs(a[i] - c[k]) + abs(b[j] - c[k]));
            }
        }
    }

    cout << ans << "\n";
}

