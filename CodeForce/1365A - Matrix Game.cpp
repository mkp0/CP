void solve()
{
    int n, m;
    cin >> n >> m;

    int mat[n][m];
    int co = 0;

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            cin >> mat[i][j];
        }
    }

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            bool row = true, col = true;

            int a = 0, b = 0;
            for (; a < n; a++)
            {
                if (mat[a][j])
                {
                    col = false;
                }
            }
            for (; b < m; b++)
            {
                if (mat[i][b])
                {
                    row = false;
                }
            }

            if (row && col)
            {
                mat[i][j] = 1;
                co++;
            }
        }
    }

    if (co % 2)
    {
        cout << "Ashish" << endl;
    }
    else
    {
        cout << "Vivek" << endl;
    }
}

